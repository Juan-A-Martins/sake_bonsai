    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        if (jTableProdutos.getSelectedRow() >=0) {
            if (JOptionPane.showConfirmDialog(this, 
                    "Tem certeza de que deseja excluir?",
                    this.getTitle(),
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                Produto produto = produtoModel.getProduto(jTableProdutos.getSelectedRow());
                if (produto !=null) {
                    SakeBonsaiCliente.ccont.excluirProduto(produto);
                    atualizaTabela();
                }
                
            }
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

    private void jTableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdutosMouseClicked
        // TODO add your handling code here:
        
        if (evt.getClickCount() == 2) {
            int linha = jTableProdutos.getSelectedRow();
            if (linha >= 0) {
                Produto produto = produtoModel.getProduto(linha);
                System.out.println(produto);
                TelaDetalharProduto detalharProduto = new TelaDetalharProduto(produto);
                detalharProduto.setVisible(true);

            }
        }

    }//GEN-LAST:event_jTableProdutosMouseClicked

