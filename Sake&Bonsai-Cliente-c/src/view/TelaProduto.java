    public void atualizaTabela(){
        ArrayList<Produto> lista;
        if (jcbmPesquisa.getSelectedIndex() == 1) {
            lista = SakeBonsaiCliente.ccont.getProdutoListaNome(jtxtNome.getText());
        } else {
            lista = SakeBonsaiCliente.ccont.listaProduto();
        }
        //JOptionPane.showMessageDialog(this, lista);
        //criando a table model e passando a lisa como parametro
        produtoModel = new ProdutoTableModel(lista);
        //informando o table model para o JTABLE
        jTableProdutos.setModel(produtoModel);
    }
    
